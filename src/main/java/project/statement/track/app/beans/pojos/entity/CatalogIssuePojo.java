package project.statement.track.app.beans.pojos.entity;

public class CatalogIssuePojo {

	private Integer id;
	
	private String initials;

	private String description;
	
	private String descriptionCustom;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getInitials() {
		return initials;
	}

	public void setInitials(String initials) {
		this.initials = initials;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getDescriptionCustom() {
		return descriptionCustom;
	}

	public void setDescriptionCustom(String descriptionCustom) {
		this.descriptionCustom = descriptionCustom;
	}
}
